class Switch{
    public static void main(String args[]){
        int n=5;
        switch(n){
            case 1:
                System.out.println("you enter 1");
                break;
            case 2:
                System.out.println("you enter 2");
                break;
            case 3:
                System.out.println("you enter 3");
                break;
            default:
                System.out.println("invalid....");
                break;

        }
    }
}